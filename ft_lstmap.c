#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*nlist;
	t_list	*nnode;
	void	*ncontent;

	nlist = NULL;
	while (lst != NULL)
	{
		ncontent = f(lst->content);
		nnode = ft_lstnew(ncontent);
		if (nnode == NULL)
		{
			ft_lstclear(&nlist, del);
			return (NULL);
		}
		ft_lstadd_back(&nlist, nnode);
		lst = lst->next;
	}
	return (nlist);
}
